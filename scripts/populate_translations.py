def populate() -> None:
    for lang, translations in TRANSLATIONS.items():
        po_path = LOCALES_DIR / lang / "LC_MESSAGES" / "hash_turbo.po"
        with open(po_path, "rb") as f:
            catalog = pofile.read_po(f, locale=lang)

        for message in catalog:
            if message.id and message.id in translations:
                message.string = translations[message.id]

        # Remove fuzzy flag from header
        if "" in catalog:
            catalog.header_comment = catalog.header_comment.replace("#, fuzzy\n", "")

        with open(po_path, "wb") as f:
            pofile.write_po(f, catalog, width=0)

        filled = sum(1 for m in catalog if m.id and m.string)
        total = sum(1 for m in catalog if m.id)
        print(f"{lang}: {filled}/{total} strings translated in {po_path}")


if __name__ == "__main__":
    populate()
